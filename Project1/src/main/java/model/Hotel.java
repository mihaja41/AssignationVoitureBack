package model;

import java.util.List;

public class Hotel {
    private Long id;
    private String name;

    public Hotel() {}

    public Hotel(String name) {
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}